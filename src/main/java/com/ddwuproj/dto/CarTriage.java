package com.ddwuproj.dto;


public class CarTriage {

    private String sido;
    private String gungu;
    private String name;
    private String addr;
    private String openWd;
    private String openSa;
    private String openSu;
    private String tel;
    private String lng;
    private String lat;
    private int category;

    public String getSido() {
        return sido;
    }

    public void setSido(String sido) {
        this.sido = sido;
    }

    public String getGungu() {
        return gungu;
    }

    public void setGungu(String gungu) {
        this.gungu = gungu;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddr() {
        return addr;
    }

    public void setAddr(String addr) {
        this.addr = addr;
    }

    public String getOpenWd() {
        return openWd;
    }

    public void setOpenWd(String openWd) {
        this.openWd = openWd;
    }

    public String getOpenSa() {
        return openSa;
    }

    public void setOpenSa(String openSa) {
        this.openSa = openSa;
    }

    public String getOpenSu() {
        return openSu;
    }

    public void setOpenSu(String openSu) {
        this.openSu = openSu;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public int getCategory() {
        return category;
    }

    public void setCategory(int category) {
        this.category = category;
    }
}
